<h1>Black Arts + Data Futures</h1>
<h3>a UW-Madison Borghesi-Mellon workshop 2017-2018</h3>
<body>


<div><p>Theorist Fred Moten introduced the notion of "fugitivity" (Harney and Moten, 2013, <a href="http://www.minorcompositions.info/?p=516">The Undercommons</a>) to describe the tactics of cultural and material resiliency by black subjects, seemingly foreclosed from the mainstream of neoliberalism. Black fugitivity is an ethos of adaptation to an otherwise racialized political economy of exile, diaspora, incarceration and assimilation. In this workshop, we attempt to map the historic and future praxis of technological adaptation in the Black diaspora, including the spiritual, material and cultural tactics of resilience and self-determination informed by such modes as Afro-Futurism and Afro-Optimism.
    The Black Futures Working Group shall hold regular colloquia during AY2017-18, examining academic writings, civic-tech case studies, and arts interventions that exemplify these themes. We will draw on New Media studies, and critically examine the role of Black arts activism as a form of material innovation (Harlem Renaissance, Negritude movement, 1960s Black Aesthetics, UK Blak, Hip Hop, Afro-Futurism, etc.). The aim of the series is to build literacies around the notion of fugitivity, and technical familiarity with these tactics in arts practice and digital media making.
    We also intend to host 2-3 scholars and artists during the course of the academic year whose work extends and challenges these frameworks. Our workshops will engage these artists/scholars, and ultimately feed the development of future coursework in our respective departments â€” including the development of new programs in new media and civic tech development, data ethics, material innovation and studio art. These subjects are also relevant to the ongoing development of undergraduate student certificate programs (minors) and support the work of research centers in the School of Information, Art, Art History, Afro-American Studies, African Cultural Studies, and Communication Arts.
</p></div>
